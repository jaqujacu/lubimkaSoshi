// === –•–†–ê–ù–ï–ù–ò–ï ===
function getData() {
    const saved = localStorage.getItem('poetryData');
    return saved ? JSON.parse(saved) : {
        bookmarks: {},
        tipShown: false,
        navTipShown: false // ‚Üê –Ω–æ–≤–æ–µ –ø–æ–ª–µ
    };
}

function saveData(data) {
    localStorage.setItem('poetryData', JSON.stringify(data));
}

// === –ì–õ–û–ë–ê–õ–¨–ù–´–ï –ü–ï–†–ï–ú–ï–ù–ù–´–ï ===
let currentPoemIndex = 0;
const visibleRange = 2;

// === –ü–û–î–°–ö–ê–ó–ö–ò ===
function showBookmarkTip() {
    const modal = document.getElementById('bookmarkTip');
    const text = modal.querySelector('.modal-text');
    text.textContent = "–¢–∞–∫ —Ç—ã –º–æ–∂–µ—à—å –ø–æ–º–µ—á–∞—Ç—å –ª—é–±–∏–º—ã–µ —Å—Ç–∏—Ö–∏";
    modal.style.display = 'flex';
    
    const btn = modal.querySelector('.modal-btn');
    btn.onclick = () => {
        modal.style.display = 'none';
    };
}

function showNavTip() {
    const modal = document.getElementById('navTip');
    if (!modal) return;
    
    modal.style.display = 'flex';
    const btn = modal.querySelector('.modal-btn');
    btn.onclick = () => {
        modal.style.display = 'none';
    };
}

// === –û–ë–ù–û–í–õ–ï–ù–ò–ï –ú–ï–ù–Æ ===
function updateNavigationMenuWithData(data) {
    const nav = document.getElementById('poetryNav');
    if (!nav) return;
    
    nav.innerHTML = '';

    const upBtn = document.createElement('div');
    upBtn.className = 'nav-arrow';
    upBtn.textContent = '‚Üë';
    upBtn.onclick = () => {
        if (currentPoemIndex > 0) {
            scrollToPoem(currentPoemIndex - 1);
        }
    };
    nav.appendChild(upBtn);

    const start = Math.max(0, currentPoemIndex - visibleRange);
    const end = Math.min(poems.length, currentPoemIndex + visibleRange + 1);

    for (let i = start; i < end; i++) {
        const poemId = `poem-${i + 1}`;
        const isBookmarked = data.bookmarks[poemId] === true;
        const isActive = i === currentPoemIndex;

        const item = document.createElement('div');
        item.className = `nav-item ${isActive ? 'active' : ''} ${isBookmarked ? 'bookmarked' : ''}`;
        item.dataset.index = i;
        
        const diff = Math.abs(i - currentPoemIndex);
        let opacity = 1;
        if (diff === 1) opacity = 0.7;
        else if (diff === 2) opacity = 0.5;
        
        item.style.opacity = opacity;
        item.textContent = isBookmarked ? `–°—Ç–∏—Ö ${i + 1} üñ§` : `–°—Ç–∏—Ö ${i + 1}`;
        item.onclick = () => scrollToPoem(i);
        nav.appendChild(item);
    }

    const downBtn = document.createElement('div');
    downBtn.className = 'nav-arrow';
    downBtn.textContent = '‚Üì';
    downBtn.onclick = () => {
        if (currentPoemIndex < poems.length - 1) {
            scrollToPoem(currentPoemIndex + 1);
        }
    };
    nav.appendChild(downBtn);
}

function updateNavigationMenu() {
    const data = getData();
    updateNavigationMenuWithData(data);
}

// === –ü–†–û–ö–†–£–¢–ö–ê ===
function scrollToPoem(index) {
    currentPoemIndex = index;
    const target = document.getElementById(`anchor-${index + 1}`);
    if (target) {
        const rect = target.getBoundingClientRect();
        const offsetTop = window.pageYOffset + rect.top - 80;
        window.scrollTo({
            top: offsetTop,
            behavior: 'smooth'
        });
    }
    updateNavigationMenu();
}

// === –†–ï–ù–î–ï–† –°–¢–ò–•–û–í ===
function renderPoems() {
    const container = document.getElementById('poemsContainer');
    const data = getData();

    poems.forEach((text, index) => {
        const poemId = `poem-${index + 1}`;
        const anchorId = `anchor-${index + 1}`;
        const isBookmarked = data.bookmarks[poemId] === true;

        const lines = text.split('\n');
        let beforeAnchor = lines.slice(0, 0).join('<br>');
        let afterAnchor = lines.slice(0).join('<br>');
        
        const poemEl = document.createElement('div');
        poemEl.className = 'poem';
        poemEl.id = poemId;
        poemEl.innerHTML = `
            <div class="poem-decor">‚ãÜÔΩ° Ôæü ‚òÅÔ∏éÔΩ°‚ãÜÔΩ° Ôæü ‚òæ Ôæü ÔΩ°‚ãÜ</div>
            <p>${beforeAnchor}</p>
            <div id="${anchorId}" class="poem-anchor"></div>
            <p>${afterAnchor}</p>
            <button class="bookmark-btn" data-poem="${poemId}">
                ${isBookmarked ? 'üñ§' : 'ü§ç'}
            </button>
        `;
        if (index < poems.length - 1) {
            poemEl.innerHTML += `<div class="poem-divider">‚úß</div>`;
        }
        container.appendChild(poemEl);
    });

    const lastPoem = document.getElementById(`poem-${poems.length}`);
    if (lastPoem) {
        const finalDecor = document.createElement('div');
        finalDecor.className = 'poem-final-decor';
        finalDecor.textContent = '‚ãÜ ÀöÔΩ°‚ãÜ‡≠®‚ô°‡≠ß‚ãÜ ÀöÔΩ°‚ãÜ';
        lastPoem.appendChild(finalDecor);
    }

    setupBookmarkButtons();
    setupActiveHighlight();
    setupBackToTop();
    setupNavToggle(); // ‚Üê –Ω–æ–≤–∞—è —Ñ—É–Ω–∫—Ü–∏—è
    updateNavigationMenu();
}

// === –ó–ê–ö–õ–ê–î–ö–ò ===
function setupBookmarkButtons() {
    const data = getData();
    let hasShownTip = data.tipShown;

    document.querySelectorAll('.bookmark-btn').forEach(btn => {
        btn.addEventListener('click', () => {
            const poemId = btn.dataset.poem;
            const isFilled = btn.textContent.trim() === 'üñ§';
            
            if (!isFilled) {
                btn.textContent = 'üñ§';
                data.bookmarks[poemId] = true;
                updateNavigationMenuWithData(data);
                
                if (!hasShownTip) {
                    showBookmarkTip();
                    hasShownTip = true;
                    data.tipShown = true;
                    saveData(data);
                }
            } else {
                btn.textContent = 'ü§ç';
                delete data.bookmarks[poemId];
                updateNavigationMenuWithData(data);
            }
            
            saveData(data);
        });
    });
}

// === –ö–ù–û–ü–ö–ê "–ù–ê–í–ï–†–•" ===
function setupBackToTop() {
    const backToTop = document.getElementById('backToTop');
    if (backToTop) {
        backToTop.addEventListener('click', () => {
            window.scrollTo({
                top: 0,
                behavior: 'smooth'
            });
        });
    }
}

function setupNavToggle() {
    const toggleBtn = document.getElementById('navToggleBtn');
    const nav = document.getElementById('poetryNav');
    
    if (!toggleBtn || !nav) return;

    // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –º–æ–±–∏–ª—å–Ω–æ–µ –ª–∏ —É—Å—Ç—Ä–æ–π—Å—Ç–≤–æ
    const isMobile = window.innerWidth <= 768;
    if (!isMobile) {
        // –ù–∞ –¥–µ—Å–∫—Ç–æ–ø–µ —É–±–∏—Ä–∞–µ–º –∫–Ω–æ–ø–∫—É –≤–æ–æ–±—â–µ
        toggleBtn.style.display = 'none';
        return;
    }

    const data = getData();
    const hasShownNavTip = data.navTipShown === true;

    toggleBtn.addEventListener('click', () => {
        const isActive = nav.classList.contains('active');
        if (isActive) {
            nav.classList.remove('active');
            toggleBtn.textContent = '+';
        } else {
            nav.classList.add('active');
            toggleBtn.textContent = '‚àí';
            
            // –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –ø–æ–¥—Å–∫–∞–∑–∫—É –¢–û–õ–¨–ö–û –≤ –ø–µ—Ä–≤—ã–π —Ä–∞–∑
            if (!hasShownNavTip) {
                setTimeout(() => {
                    showNavTip();
                    data.navTipShown = true;
                    saveData(data);
                }, 300);
            }
        }
    });
}

// === –ü–û–î–°–í–ï–¢–ö–ê –ê–ö–¢–ò–í–ù–û–ì–û –°–¢–ò–•–ê ===
function setupActiveHighlight() {
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting && entry.intersectionRatio >= 0.3) {
                const id = entry.target.id;
                const index = parseInt(id.split('-')[1]) - 1;
                if (index !== currentPoemIndex) {
                    currentPoemIndex = index;
                    updateNavigationMenu();
                }
            }
        });
    }, { threshold: 0.3 });

    document.querySelectorAll('.poem').forEach(poem => {
        observer.observe(poem);
    });
}

